REDIS_MAX_CONNECTIONS=100

# ==================== CELERY ====================
CELERY_BROKER_URL={redis_urls['CELERY_BROKER_URL']}
CELERY_RESULT_BACKEND={redis_urls['CELERY_RESULT_BACKEND']}
CELERY_WORKER_CONCURRENCY=8

# ==================== SERVER ====================
